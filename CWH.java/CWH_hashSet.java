import java.util.HashSet;

public class CWH_hashSet {
    public static void main(String[] args) {
        HashSet<Integer> hs = new HashSet<>();
        hs.add(1);
        hs.add(4);
        hs.add(17);
        hs.add(3);
        hs.add(9);
        hs.add(93);
        System.out.println(hs);
    }
}
